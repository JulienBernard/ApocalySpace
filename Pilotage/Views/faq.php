	<p class="lead">ApocalySpace BETA FERME - Foire Aux Questions</p>
	<p>
		ApocalySpace est un jeu en ligne jouable par navigateur internet combinant les dernières technologies du web. Jeu multijoueur se déroulant dans un monde persistant, ApocalySpace permet à différents joueurs de se retrouver et jouer ensemble sur un même serveur !
	</p>
	
	<h1>Navigation</h1>
	<p>
		Vous pouvez naviguer sur le site par le biais de trois menus :<br />
		- Sur le haut du site, à droite, se trouve les raccourcis rapides pour les pages Compte, Communications (Messages) et Déconnexion<br />
		- En survolant le titre de la page, en haut au centre, vous ferez apparaître le menu principale (Faction, Carte, Classement - Recherches, Population, Structures - Atelier, Flottes, Usine)<br />
		- En dessous du haut du site, à gauche, se trouve les raccourcis rapides pour les pages Population, Compte, Communications (Messages) et Déconnexion
	</p>
	<p>
		La plupart des actions se font directement avec le clic gauche de votre souris, mais certaines actions peuvent nécessiter d'attendre un cours instant l'apparition d'une fenêtre par exemple !
	</p>
	<h1>Durée de vie</h1>
	<p>
		ApocalySpace est basé sur un système de factions, au nombre de trois, qui peuvent de temps en temps rentrer en guerre entre-elles, offrant alors de nouvelles fonctionnalités aux joueurs.<br />
		La fin du serveur arrivera donc le jour ou une faction aura pris le pouvoir : par la voie démographique, technologique ou militaire ...
	</p>
	
	<h1>Factions</h1>
	<p>
		La galaxie est dominé par trois factions, chacune ayant des intêrets et des idéaux différents.<br />
		- Les <span class="bold">Impériaux</span> se rapprochent le plus d'un empire totalitaire où l'ordre est signe de progrès<br />
		- Les <span class="bold">Vagabonds</span> forment un peuple libre où le mot discorde rime avec fraternité<br />
		- Les <span class="bold">Républicains</span> sont les derniers démocrates de la galaxie, ils ont foi en l'avenir du peuple
	</p>
	
